package Model;

/**
 * The enum Game state.
 */
public enum GameState {
    /**
     * Start game state.
     */
    START,
    /**
     * Take card game state.
     */
    TAKE_CARD,
    /**
     * Deck game state.
     */
    DECK,
    /**
     * Defausse game state.
     */
    DEFAUSSE,
    /**
     * Drop game state.
     */
    DROP,
    /**
     * End game state.
     */
    END
}
